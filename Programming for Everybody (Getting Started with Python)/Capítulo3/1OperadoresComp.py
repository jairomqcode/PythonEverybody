"""

Operadores de Comparación:

A == B, A es exactamente igual a B.
A != B, A es distinto de B.
A > B, A es mayor que B.
A < B, A es menor que B.
A >= B, A es mayor o igual que B.
A <= B, A es menor o igual que B.
A is B, A es lo mismo que B.
A is not B, A no es lo mismo que B.

"""

# Definimos dos variables:
a = 2
b = 4

# Operadores de comparación:
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(b >= a)
print(a <= b)
print(a is b)
print(a is not b)