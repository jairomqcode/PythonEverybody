"""

Bucle For.

for variable in elemento_iterable(lista, rango, etc):
    Bloque de instrucciones

* Se ejecutará el bucle hasta que el elemento iterable se términe (Bucle definido).

"""

princesas = ["Nicole", "Laurent"]

for hijas in princesas:
    print("Te amo ", hijas)
print("Las amo!!")


