"""
- Valor: es una de las cosas básicas que utiliza un programa, como una letra o un número.

- Variable: es un nombre que se refiere a un valor. Es un contenedor de información.

- Sentencia de asignación (=): crea variables nuevas y les da valores.

- Tipos de variables: es el tipo del valor al que se refiere, y se obtiene a través de la función type().

- Tipos:
1. String (str): contiene una cadena de letras, encerradas entre comillas dobles o simples.

2. Integer (int): es un valor o dato numérico de clase entero.

3. Float (float): es un valor o dato numérico de clase punto flotante, es decir, número que contienen parte decimal.

"""
#Definimos tres variables diferentes:
variable_1 = "Hola nico y lau"  #Variable tipo string.

variable_2 = 7                              #Variable tipo int.

variable_3 = 3.1416                     #Variable tipo float.

# Imprimimos cada variable definida y consultamos el tipo de esas variables:
print(variable_1)
print(type(variable_1))
print(variable_2)
print(type(variable_2))
print(variable_3)
print(type(variable_3))
